    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        String palabra = jTextField1.getText();
        if (palabra.equals("")) {
            JOptionPane.showMessageDialog(null, "Debes ingresar una palabra para verificar.");
        } else {
            Node buscado = funcion.tabla.search(palabra);
            if (buscado != null) {
                jTextArea1.setText(buscado.getWord() + ": " + buscado.getRepeat());
            } else {
                jTextArea1.setText("La palabra ingresada no se encuentra en el art√≠culo.");
            }
        }
    }//GEN-LAST:event_jButton3ActionPerformed


